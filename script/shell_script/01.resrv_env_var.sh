#!/bin/sh

echo '$HOME      사용자 홈 디렉터리 '
echo "$HOME      $HOME"

echo '$PATH      명령어 or 셸 실행 시 실행 파일 디렉터리 경로 '$PATH 
echo "$PATH      $PATH"

echo '$LANG      기본으로 제공해주는 언어 '$LANG


echo '$PWD       사용자의 현재 작업 디렉터리 '$PWD


echo '$TERM      로그인 터미널 타입 '$TERM


echo '$SHELL     BASH 실행 파일 경로 '$SHELL


echo '$USER      로그인한 사용자 이름'$USER


echo '$USERNAME  로그인한 사용자 이름 '$USERNAME


echo '$GROUP     결과가 없는 건 소속 그룹이 없음을 의미 '$GROUP


echo '$DISPLAY   X 디스플레이 이름 '$DISPLAY
